/** Quality display keys used across list/detail pages. */
export function qualityNameKey(n: number): string {
  return `LC_COMMON_quality_name_${n}`
}

export function forceCardQualityNameKey(n: number): string {
  const keys: Record<number, string> = {
    2: 'LC_Dynamis_Green',
    3: 'LC_Dynamis_Blue',
    4: 'LC_Dynamis_Purple',
    5: 'LC_Dynamis_Orange',
    6: 'LC_Dynamis_Red',
  }
  return keys[n] ?? `LC_Dynamis_Green`
}

